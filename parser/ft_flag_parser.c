#include "../base/ft_printf.h"

void	ft_flag_pars(char ****ps, t_parser *pos)
{
	while (****ps == '0' || ****ps == '-')
	{
		if (****ps == '0')
			pos->flag = '0';
		else if (****ps == '-')
		{
			pos->flag = '-';
			while (****ps == '-' || ****ps == '0')
			{
				(***ps)++;
				if (****ps != '-' && ****ps != '0')
					return ;
			}
		}
		(***ps)++;
	}
}
